def get_board_from_state(board, state: GameState):
    if isinstance(state, str):
        return
    temp_board = deepcopy(board)
    temp_board[state.row][state.col] = 'c'
    if is_parking(board, state.row, state.col - 1):
        temp_board[state.row][state.col - 1] = 1 if state.l else 0
    if is_parking(board, state.row, state.col + 1):
        temp_board[state.row][state.col + 1] = 1 if state.r else 0
    if is_parking(board, state.row - 1, state.col):
        temp_board[state.row - 1][state.col] = 1 if state.u else 0
    if is_parking(board, state.row + 1, state.col):
        temp_board[state.row + 1][state.col] = 1 if state.d else 0
    return temp_board


def runEpisodeWithDisplay(agent, environment, discount, decision, display, message, pause, episode, board):
    returns = 0
    totalDiscount = 1.0
    environment.reset()
    steps_counter = 0
    if 'startEpisode' in dir(agent): agent.startEpisode()
    message("BEGINNING EPISODE: " + str(episode) + "\n")
    while True:

        state = environment.getCurrentState()
        pause()

        # END IF IN A TERMINAL STATE
        actions = environment.getPossibleActions(state)
        isFinished = False
        if actions == [Action.EXIT]:
            isFinished = True
        if not actions:
            message("EPISODE " + str(episode) + " COMPLETE: RETURN WAS " + str(returns) + "\n")
            print("num steps - ", steps_counter)
            graphicsUtils.sleep(1)
            return returns

        # GET ACTION (USUALLY FROM AGENT)
        action = decision(state)
        if action == None:
            raise 'Error: Agent returned None action'

        # DISPLAY CURRENT STATE
        display.drawGrid(get_board_from_state(board, state), isFinished, action)
        graphicsUtils.sleep(0.2)

        # EXECUTE ACTION
        nextState, reward = environment.doAction(action)
        steps_counter+=1
        message("Started in state: " + str(state) +
                "\nTook action: " + str(action) +
                "\nEnded in state: " + str(nextState) +
                "\nGot reward: " + str(reward) + "\n")

        returns += reward * totalDiscount
        totalDiscount *= discount

        if 'stopEpisode' in dir(agent):
            agent.stopEpisode()


def choose_parkings(probability_parking_map):
    parking_map = deepcopy(probability_parking_map)
    for i in range(len(parking_map)):
        for j in range(len(parking_map[i])):
            if isinstance(parking_map[i][j], float):
                parking_map[i][j] = 1 if random.random() < parking_map[i][j] else 0
    return parking_map


